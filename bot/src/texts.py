def format_personal_data(
    me: dict,
    balances: dict,
    invested_usdt: str = "0.00",
    invested_usdc: str = "0.00",
    ref_link: str | None = None,
) -> str:
    """Формирует блок «Личные данные» для второго сообщения при /start."""
    usdt = float(balances.get("USDT", 0) or 0)
    usdc = float(balances.get("USDC", 0) or 0)
    usdt_s = f"{usdt:.2f}"
    usdc_s = f"{usdc:.2f}"

    name = me.get("name") or me.get("username") or "не указано"
    email = me.get("email") or "не указано"
    country = me.get("country") or "не указано"
    referrals = me.get("referrals_count", 0)
    ref_code = me.get("ref_code", "—")

    if ref_link:
        ref_block = f"🔗 Реферальная ссылка:\n{ref_link}"
    else:
        ref_block = f"🔗 Реферальный код: <code>{ref_code}</code>"

    return (
        "<b>Личные данные:</b>\n\n"
        "💰 <b>Баланс:</b>\n"
        f"USDT: {usdt_s}\n"
        f"USDC: {usdc_s}\n\n"
        "🔒 <b>Инвестировано:</b>\n"
        f"USDT: {invested_usdt}\n"
        f"USDC: {invested_usdc}\n\n"
        f"👤 Имя: {name}\n"
        f"📧 Email: {email}\n"
        f"🌍 Страна: {country}\n\n"
        f"👥 Рефералы: {referrals}\n"
        f"{ref_block}"
    )
